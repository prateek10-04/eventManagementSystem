    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String event=jComboBox1.getSelectedItem().toString();
        if(txtFeedback.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Enter Name");
            txtFeedback.requestFocus();
        }
        else{
             
    Connection con = null;
    PreparedStatement pst = null;
    ResultSet rs = null;
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/eventmanagement", "root", "Babaji@10");
    

    // Insert into participant table
    pst = con.prepareStatement("INSERT INTO feedback(Feedback,ParticipantID,Rating,EventName) VALUES(?, ?,?,?)");
    pst.setString(1, txtFeedback.getText());
    pst.setInt(2, participantid);
    pst.setFloat(3 , Float.parseFloat(txtRating.getText()));
    pst.setString(4, event);
    int rowsAffected = pst.executeUpdate(); 

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Feedback Submitted!");
                //new adminAddEvents(participantID).setVisible(true);
                new Participant(participantid).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        
    


    } catch (ClassNotFoundException | SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    } finally {
        try {
            if (rs != null) rs.close();
            if (pst != null) pst.close();
            if (con != null) con.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        
    }
}
        }
    }//GEN-LAST:event_jButton2ActionPerformed

